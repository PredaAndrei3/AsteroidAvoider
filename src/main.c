int main() {
    joystick_init();
    systime_init();
    sei();

    ili9341_240x320_spi_init(RESET_PIN, CS_PIN, DC_PIN);
    ili9341_setRotation(3);
    ssd1306_setMode(LCD_MODE_NORMAL);
    ssd1306_fillScreen16(BACKGROUND_COLOR);

    float player_x = 100, player_y = 100;
    uint16_t old_x_draw = 100, old_y_draw = 100;
    ssd1306_drawBitmap16(old_x_draw, old_y_draw, 23, 24, (uint8_t*)spaceship_bitmap);

    uint32_t old_ms_value = 0;

    while (1) {
        uint32_t ms_value = systime_get_ms();
        float delta_time = (ms_value - old_ms_value) / 1000.0f;
        old_ms_value = ms_value;

        player_x += joystick_get_x() * delta_time;
        player_y += joystick_get_y() * delta_time;

        uint16_t x_draw = round(player_x);
        uint16_t y_draw = round(player_y);

        if (x_draw != old_x_draw || y_draw != old_y_draw) {
            ssd1306_drawBitmap16(x_draw, y_draw, 23, 24, (uint8_t*)spaceship_bitmap);

            ssd1306_setColor(BACKGROUND_COLOR);

            int16_t old_index = 0;
            int16_t index_offset = 23 * (y_draw - old_y_draw) + (x_draw - old_x_draw);

            for (int16_t old_y = old_y_draw; old_y < old_y_draw + 24; old_y++) {
                for (int16_t old_x = old_x_draw; old_x < old_x_draw + 23; old_x++) {
                    if (pgm_read_word(&spaceship_bitmap[old_index]) == 0x6510) {
                        old_index++;
                        continue;
                    }

                    bool intersection = false;

                    if (old_x >= x_draw && old_x < x_draw + 23
                        && old_y >= y_draw && old_y < y_draw + 24) {
                        int16_t index = old_index - index_offset;

                        if (index >= 0 && index < 23 * 24 && pgm_read_word(&spaceship_bitmap[index]) != 0x6510) {
                            intersection = true;
                        }
                    }

                    if (!intersection) {
                        ssd1306_putPixel16(old_x, old_y);
                    }

                    old_index++;
                }
            }

            old_x_draw = x_draw;
            old_y_draw = y_draw;
        }
    }

    return 0;
}